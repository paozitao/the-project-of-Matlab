n=0:1:15;
x1=cos(n*pi/4);
x2=sin(n*pi/8);
x=x1+1i*x2;
X=fft(x,16);
Xx=conj([X(1),fliplr(X(2:16))]);
Xoe=(X+Xx)/2;
Xop=(X-Xx)/2/1i;
X1=fft(x1,16);
X2=fft(x2,16);
subplot(231);
stem(n,X1);
title('cos(n*pi/4)的DFT');
subplot(232);
stem(n,Xoe);
title('X的共轭对称部分');
subplot(233);
stem(n,ifft(Xoe));
title('cos(n*pi/4)的IDFT');
subplot(234);
stem(n,abs(X2));
title('sin(n*pi/8)的DFT');
subplot(235);
stem(n,abs(Xop));
title('X的共轭反对称部分');
subplot(236);
stem(n,ifft(Xop));
title('sin(n*pi/8)的IDFT');